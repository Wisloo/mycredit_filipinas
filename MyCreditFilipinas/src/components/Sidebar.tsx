"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";

interface SidebarLink {
  href: string;
  label: string;
  icon: React.ReactNode;
}

interface SidebarProps {
  links: SidebarLink[];
  collapsed?: boolean;
}

export default function Sidebar({ links, collapsed }: SidebarProps) {
  const pathname = usePathname();

  return (
    <aside
      className={`bg-white border-r border-gray-100 min-h-[calc(100vh-4rem)] transition-all duration-300 ${
        collapsed ? "w-16" : "w-64"
      } hidden lg:block shadow-sm`}
    >
      <nav className="py-6 space-y-1 px-3">
        {links.map((link) => {
          const active = link.href === "/admin" || link.href === "/dashboard"
            ? pathname === link.href
            : pathname === link.href || pathname.startsWith(link.href + "/");
          return (
            <Link
              key={link.href}
              href={link.href}
              className={`flex items-center px-3 py-3 text-base font-medium rounded-xl transition-all duration-200 group ${
                active
                  ? "bg-gradient-to-r from-ph-blue-50 to-ph-blue-100/50 text-ph-blue-600 shadow-sm shadow-ph-blue-100"
                  : "text-gray-500 hover:bg-gray-50 hover:text-gray-700"
              }`}
            >
              <span className={`flex-shrink-0 w-5 h-5 mr-3 transition-colors duration-200 ${active ? "text-ph-blue-500" : "text-gray-400 group-hover:text-gray-600"}`}>
                {link.icon}
              </span>
              {!collapsed && <span>{link.label}</span>}
              {active && !collapsed && (
                <span className="ml-auto w-1.5 h-1.5 bg-ph-blue-500 rounded-full" />
              )}
            </Link>
          );
        })}
      </nav>
    </aside>
  );
}

